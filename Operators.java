public class Operators{  
    public static void main(String args[]){  
        int a=100;  
        int b=55;  
        System.out.println(a+b);//155 
        System.out.println(a-b);//45  
        System.out.println(a*b);//5500  
        System.out.println(a/b);//1.82 
        System.out.println(a%b);//45  
    }
} 